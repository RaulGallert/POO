public class Atv02 {
    public static void main(String[] args) {
        int a = 2147483647;
        long b = 543758998453484L;
        float c = 5.75f;
        double d = 10.45;
        boolean e = true;
        byte f = 127;
        short g = 32767;
        char h = 'c';
        System.out.println("int: "+a);
        System.out.println("long: "+b);
        System.out.println("float: "+c);
        System.out.println("double: "+d);
        System.out.println("bolean: "+e);
        System.out.println("byte: "+f);
        System.out.println("short: "+g);
        System.out.println("char: "+h);
        String oi = "tudo bem?\n";
        String sim = "como\t vai";
        String agns = "aa\"foca\"aaa";
        String afs = "ola\\r";
        System.out.println("Pula linha: " +oi);
        System.out.println("Tabulação: " +sim);
        System.out.println("Aspas: " +agns);
        System.out.println("Barra invertida: " +afs);
    }
}